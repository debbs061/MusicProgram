package com.exe.domain;

public class Singer {
	
	private String singer_key;
	private String singer_name;
	private String debut_date;
	private String agency;
	
	public String getSinger_key() {
		return singer_key;
	}
	public void setSinger_key(String singer_key) {
		this.singer_key = singer_key;
	}
	public String getSinger_name() {
		return singer_name;
	}
	public void setSinger_name(String singer_name) {
		this.singer_name = singer_name;
	}
	public String getDebut_date() {
		return debut_date;
	}
	public void setDebut_date(String debut_date) {
		this.debut_date = debut_date;
	}
	public String getAgency() {
		return agency;
	}
	public void setAgency(String agency) {
		this.agency = agency;
	}
	
	

}
